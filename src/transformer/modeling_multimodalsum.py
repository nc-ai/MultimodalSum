class EncoderLayer(nn.Module):
    def __init__(self, config: BartConfig):
        super().__init__()
        self.embed_dim = config.d_model
        self.self_attn = SelfAttention(
            self.embed_dim, config.encoder_attention_heads, dropout=config.attention_dropout,
        )
        self.normalize_before = config.normalize_before
        self.self_attn_layer_norm = LayerNorm(self.embed_dim)
        self.dropout = config.dropout
        self.activation_fn = ACT2FN[config.activation_function]
        self.activation_dropout = config.activation_dropout
        self.fc1 = nn.Linear(self.embed_dim, config.encoder_ffn_dim)
        self.fc2 = nn.Linear(config.encoder_ffn_dim, self.embed_dim)
        self.final_layer_norm = LayerNorm(self.embed_dim)

    def forward(self, x, encoder_padding_mask, output_attentions=False):
        """
        Args:
            x (Tensor): input to the layer of shape `(seq_len, batch, embed_dim)`
            encoder_padding_mask (ByteTensor): binary ByteTensor of shape
                `(batch, src_len)` where padding elements are indicated by ``1``.
            for t_tgt, t_src is excluded (or masked out), =0 means it is
            included in attention

        Returns:
            encoded output of shape `(seq_len, batch, embed_dim)`
        """
        residual = x
        if self.normalize_before:
            x = self.self_attn_layer_norm(x)
        x, attn_weights = self.self_attn(
            query=x, key=x, key_padding_mask=encoder_padding_mask, output_attentions=output_attentions
        )
        x = F.dropout(x, p=self.dropout, training=self.training)
        x = residual + x
        if not self.normalize_before:
            x = self.self_attn_layer_norm(x)

        residual = x
        if self.normalize_before:
            x = self.final_layer_norm(x)
        x = self.activation_fn(self.fc1(x))
        x = F.dropout(x, p=self.activation_dropout, training=self.training)
        x = self.fc2(x)
        x = F.dropout(x, p=self.dropout, training=self.training)
        x = residual + x
        if not self.normalize_before:
            x = self.final_layer_norm(x)
        return x, attn_weights


class BartEncoder(nn.Module):
    """
    Transformer encoder consisting of *config.encoder_layers* self attention layers. Each layer
    is a :class:`EncoderLayer`.

    Args:
        config: BartConfig
    """

    def __init__(self, config: BartConfig, embed_tokens):
        super().__init__()

        self.dropout = config.dropout
        self.layerdrop = config.encoder_layerdrop

        embed_dim = embed_tokens.embedding_dim
        self.embed_scale = math.sqrt(embed_dim) if config.scale_embedding else 1.0
        self.padding_idx = embed_tokens.padding_idx
        self.max_source_positions = config.max_position_embeddings

        self.embed_tokens = embed_tokens
        if config.static_position_embeddings:
            self.embed_positions = SinusoidalPositionalEmbedding(
                config.max_position_embeddings, embed_dim, self.padding_idx
            )
        else:
            self.embed_positions = LearnedPositionalEmbedding(
                config.max_position_embeddings, embed_dim, self.padding_idx, config.extra_pos_embeddings,
            )
        self.layers = nn.ModuleList([EncoderLayer(config) for _ in range(config.encoder_layers)])
        self.layernorm_embedding = LayerNorm(embed_dim) if config.normalize_embedding else nn.Identity()
        # mbart has one extra layer_norm
        self.layer_norm = LayerNorm(config.d_model) if config.normalize_before else None

    def forward(
        self, input_ids, attention_mask=None, output_attentions=False, output_hidden_states=False, return_dict=False
    ):
        """
        Args:
            input_ids (LongTensor): tokens in the source language of shape
                `(batch, src_len)`
            attention_mask (torch.LongTensor): indicating which indices are padding tokens.
        Returns:
            BaseModelOutput or Tuple comprised of:
                - **x** (Tensor): the last encoder layer's output of
                  shape `(src_len, batch, embed_dim)`
                - **encoder_states** (tuple(torch.FloatTensor)): all intermediate
                  hidden states of shape `(src_len, batch, embed_dim)`.
                  Only populated if *output_hidden_states:* is True.
                - **all_attentions** (tuple(torch.FloatTensor)): Attention weights for each layer.
                During training might not be of length n_layers because of layer dropout.
        """
        # check attention mask and invert
        if attention_mask is not None:
            attention_mask = invert_mask(attention_mask)

        inputs_embeds = self.embed_tokens(input_ids) * self.embed_scale
        embed_pos = self.embed_positions(input_ids)
        x = inputs_embeds + embed_pos
        x = self.layernorm_embedding(x)
        x = F.dropout(x, p=self.dropout, training=self.training)

        # B x T x C -> T x B x C
        x = x.transpose(0, 1)

        encoder_states = [] if output_hidden_states else None
        all_attentions = () if output_attentions else None
        for encoder_layer in self.layers:
            if output_hidden_states:
                encoder_states.append(x)
            # add LayerDrop (see https://arxiv.org/abs/1909.11556 for description)
            dropout_probability = random.uniform(0, 1)
            if self.training and (dropout_probability < self.layerdrop):  # skip the layer
                attn = None
            else:
                x, attn = encoder_layer(x, attention_mask, output_attentions=output_attentions)

            if output_attentions:
                all_attentions = all_attentions + (attn,)

        if self.layer_norm:
            x = self.layer_norm(x)
        if output_hidden_states:
            encoder_states.append(x)
            # T x B x C -> B x T x C
            encoder_states = tuple(hidden_state.transpose(0, 1) for hidden_state in encoder_states)

        # T x B x C -> B x T x C
        x = x.transpose(0, 1)

        if not return_dict:
            return tuple(v for v in [x, encoder_states, all_attentions] if v is not None)
        return BaseModelOutput(last_hidden_state=x, hidden_states=encoder_states, attentions=all_attentions)


class DecoderLayer(nn.Module):
    def __init__(self, config: BartConfig, multimodal=False):
        super().__init__()
        self.embed_dim = config.d_model
        self.self_attn = SelfAttention(
            embed_dim=self.embed_dim, num_heads=config.decoder_attention_heads, dropout=config.attention_dropout,
        )
        self.dropout = config.dropout
        self.activation_fn = ACT2FN[config.activation_function]
        self.activation_dropout = config.activation_dropout
        self.normalize_before = config.normalize_before

        self.self_attn_layer_norm = LayerNorm(self.embed_dim)
        self.encoder_attn = SelfAttention(
            self.embed_dim,
            config.decoder_attention_heads,
            dropout=config.attention_dropout,
            encoder_decoder_attention=True,
            multimodal=multimodal
        )
        self.encoder_attn_layer_norm = LayerNorm(self.embed_dim)
        self.fc1 = nn.Linear(self.embed_dim, config.decoder_ffn_dim)
        self.fc2 = nn.Linear(config.decoder_ffn_dim, self.embed_dim)
        self.final_layer_norm = LayerNorm(self.embed_dim)

    def forward(
        self,
        x,
        encoder_hidden_states,
        encoder_attn_mask=None,
        layer_state=None,
        causal_mask=None,
        decoder_padding_mask=None,
        output_attentions=False,
    ):
        residual = x

        if layer_state is None:
            layer_state = {}
        if self.normalize_before:
            x = self.self_attn_layer_norm(x)
        # Self Attention

        x, self_attn_weights = self.self_attn(
            query=x,
            key=x,
            layer_state=layer_state,  # adds keys to layer state
            key_padding_mask=decoder_padding_mask,
            attn_mask=causal_mask,
            output_attentions=output_attentions,
        )
        x = F.dropout(x, p=self.dropout, training=self.training)
        x = residual + x
        if not self.normalize_before:
            x = self.self_attn_layer_norm(x)

        # Cross attention
        residual = x
        assert self.encoder_attn.cache_key != self.self_attn.cache_key
        if self.normalize_before:
            x = self.encoder_attn_layer_norm(x)
        x, _ = self.encoder_attn(
            query=x,
            key=encoder_hidden_states,
            key_padding_mask=encoder_attn_mask,
            layer_state=layer_state,  # mutates layer state
        )
        x = F.dropout(x, p=self.dropout, training=self.training)
        x = residual + x
        if not self.normalize_before:
            x = self.encoder_attn_layer_norm(x)

        # Fully Connected
        residual = x
        if self.normalize_before:
            x = self.final_layer_norm(x)
        x = self.activation_fn(self.fc1(x))
        x = F.dropout(x, p=self.activation_dropout, training=self.training)
        x = self.fc2(x)
        x = F.dropout(x, p=self.dropout, training=self.training)
        x = residual + x
        if not self.normalize_before:
            x = self.final_layer_norm(x)
        return (
            x,
            self_attn_weights,
            layer_state,
        )  # just self_attn weights for now, following t5, layer_state = cache for decoding


class BartDecoder(nn.Module):
    """
    Transformer decoder consisting of *config.decoder_layers* layers. Each layer
    is a :class:`DecoderLayer`.
    Args:
        config: BartConfig
        embed_tokens (torch.nn.Embedding): output embedding
    """

    def __init__(self, config: BartConfig, embed_tokens: nn.Embedding, multimodal=False):
        super().__init__()
        self.dropout = config.dropout
        self.layerdrop = config.decoder_layerdrop
        self.padding_idx = embed_tokens.padding_idx
        self.max_target_positions = config.max_position_embeddings
        self.embed_scale = math.sqrt(config.d_model) if config.scale_embedding else 1.0
        self.embed_tokens = embed_tokens
        init_rating_embeddings = torch.empty(config.d_model).data.normal_(mean=0.0, std=config.init_std)
        self.rating_embeddings = nn.Parameter(init_rating_embeddings)
        if config.static_position_embeddings:
            self.embed_positions = SinusoidalPositionalEmbedding(
                config.max_position_embeddings, config.d_model, config.pad_token_id
            )
        else:
            self.embed_positions = LearnedPositionalEmbedding(
                config.max_position_embeddings, config.d_model, self.padding_idx, config.extra_pos_embeddings,
            )
        self.layers = nn.ModuleList(
            [DecoderLayer(config, multimodal) for _ in range(config.decoder_layers)]
        )  # type: List[DecoderLayer]
        self.layernorm_embedding = LayerNorm(config.d_model) if config.normalize_embedding else nn.Identity()
        self.layer_norm = LayerNorm(config.d_model) if config.add_final_layer_norm else None

    def forward(
        self,
        input_ids,
        encoder_hidden_states,
        encoder_padding_mask,
        decoder_padding_mask,
        decoder_causal_mask,
        rating_diff=None,
        decoder_past_key_values=None,
        use_cache=False,
        output_attentions=False,
        output_hidden_states=False,
        return_dict=False,
        **unused,
    ):
        """
        Includes several features from "Jointly Learning to Align and
        Translate with Transformer Models" (Garg et al., EMNLP 2019).

        Args:
            input_ids (LongTensor): previous decoder outputs of shape
                `(batch, tgt_len)`, for teacher forcing
            encoder_hidden_states: output from the encoder, used for
                encoder-side attention
            encoder_padding_mask: for ignoring pad tokens
            decoder_past_key_values (dict or None): dictionary used for storing state during generation

        Returns:
            BaseModelOutputWithPast or tuple:
                - the decoder's features of shape `(batch, tgt_len, embed_dim)`
                - the cache
                - hidden states
                - attentions
        """
        if "decoder_cached_states" in unused:
            warnings.warn(
                "The `decoder_cached_states` argument is deprecated and will be removed in a future version, use `decoder_past_key_values` instead.",
                FutureWarning,
            )
            decoder_past_key_values = unused.pop("decoder_cached_states")

        # check attention mask and invert
        if type(encoder_padding_mask) == list:
            for i in range(len(encoder_padding_mask)):
                if encoder_padding_mask[i] is not None:
                    encoder_padding_mask[i] = invert_mask(encoder_padding_mask[i])
        else:
            if encoder_padding_mask is not None:
                encoder_padding_mask = invert_mask(encoder_padding_mask)

        # embed positions
        positions = self.embed_positions(input_ids, use_cache=use_cache)

        if use_cache:
            input_ids = input_ids[:, -1:]
            positions = positions[:, -1:]  # happens after we embed them
            # assert input_ids.ne(self.padding_idx).any()

        x = self.embed_tokens(input_ids) * self.embed_scale
        x += positions

        if rating_diff is not None:
            rating_diff = (rating_diff * self.rating_embeddings).unsqueeze(1)
            x += rating_diff
        else:
            self.rating_embeddings.require_grad = False
        x = self.layernorm_embedding(x)
        x = F.dropout(x, p=self.dropout, training=self.training)

        # Convert to Bart output format: (BS, seq_len, model_dim) -> (seq_len, BS, model_dim)
        #                                (BS, num_entities, seq_len, model_dim) -> (seq_len, num_entities, BS, model_dim)
        x = x.transpose(0, -2)
        if type(encoder_hidden_states) == list:
            for i in range(len(encoder_hidden_states)):
                encoder_hidden_states[i] = encoder_hidden_states[i].transpose(0, -2)
        else:
            encoder_hidden_states = encoder_hidden_states.transpose(0, -2)

        # decoder layers
        all_hidden_states = () if output_hidden_states else None
        all_self_attns = () if output_attentions else None
        next_decoder_cache = []
        for idx, decoder_layer in enumerate(self.layers):
            # add LayerDrop (see https://arxiv.org/abs/1909.11556 for description)
            if output_hidden_states:
                all_hidden_states += (x,)
            dropout_probability = random.uniform(0, 1)
            if self.training and (dropout_probability < self.layerdrop):
                continue

            layer_state = decoder_past_key_values[idx] if decoder_past_key_values is not None else None

            x, layer_self_attn, layer_past = decoder_layer(
                x,
                encoder_hidden_states,
                encoder_attn_mask=encoder_padding_mask,
                decoder_padding_mask=decoder_padding_mask,
                layer_state=layer_state,
                causal_mask=decoder_causal_mask,
                output_attentions=output_attentions,
            )

            if use_cache:
                next_decoder_cache.append(layer_past.copy())

            if self.layer_norm and (idx == len(self.layers) - 1):  # last layer of mbart
                x = self.layer_norm(x)
            if output_attentions:
                all_self_attns += (layer_self_attn,)

        # Convert to standard output format: (seq_len, BS, model_dim) -> (BS, seq_len, model_dim)
        #                                    (seq_len, num_entities, BS, model_dim) -> (BS, num_entities, seq_len, model_dim)
        if output_hidden_states:
            all_hidden_states = tuple(hidden_state.transpose(0, -2) for hidden_state in all_hidden_states)
        x = x.transpose(0, -2)
        if type(encoder_hidden_states) == list:
            for i in range(len(encoder_hidden_states)):
                encoder_hidden_states[i] = encoder_hidden_states[i].transpose(0, -2)
        else:
            encoder_hidden_states = encoder_hidden_states.transpose(0, -2)

        if use_cache:
            next_cache = ((encoder_hidden_states, encoder_padding_mask), next_decoder_cache)
        else:
            next_cache = None

        if not return_dict:
            return tuple(v for v in [x, next_cache, all_hidden_states, all_self_attns] if v is not None)
        return BaseModelOutputWithPast(
            last_hidden_state=x, past_key_values=next_cache, hidden_states=all_hidden_states, attentions=all_self_attns
        )


def _reorder_buffer(attn_cache, new_order):
    for k, input_buffer_k in attn_cache.items():
        if input_buffer_k is not None:
            # saved states are stored with shape (bsz, num_heads, seq_len, head_dim)
            # saved states are stored with shape (num_entities, bsz, num_heads, seq_len, head_dim)
            attn_cache[k] = input_buffer_k.index_select(-4, new_order)
    return attn_cache


class SelfAttention(nn.Module):
    """Multi-headed attention from 'Attention Is All You Need' paper"""

    def __init__(
        self,
        embed_dim,
        num_heads,
        num_entities=1,
        dropout=0.0,
        bias=True,
        encoder_decoder_attention=False,  # otherwise self_attention
        multimodal=False
    ):
        super().__init__()
        self.embed_dim = embed_dim
        self.num_heads = num_heads
        self.num_entities = num_entities
        self.dropout = dropout
        self.head_dim = embed_dim // num_heads
        assert self.head_dim * num_heads == self.embed_dim, "embed_dim must be divisible by num_heads"
        self.scaling = self.head_dim ** -0.5

        self.encoder_decoder_attention = encoder_decoder_attention
        self.k_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.v_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.q_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.out_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.cache_key = "encoder_decoder" if self.encoder_decoder_attention else "self"

        if self.encoder_decoder_attention:
            if multimodal:
                self.alpha_proj = nn.Linear(self.embed_dim*2, self.embed_dim, bias=bias)
                self.beta_proj = nn.Linear(self.embed_dim*2, self.embed_dim, bias=bias)

    def _shape(self, tensor, seq_len, bsz, mode):
        if mode == 'q' and self.encoder_decoder_attention:
            tensor = tensor.unsqueeze(0).repeat(self.num_entities, 1, 1, 1).transpose(0, 1)       
        return tensor.contiguous().view(seq_len, self.num_entities * bsz * self.num_heads, self.head_dim).transpose(0, 1)

    def forward(
        self,
        query,
        key,
        key_padding_mask = None, # decoder_padding_mask(self_attention) / encoder_attn_mask (cross_attention)
        layer_state = None,
        attn_mask = None, # causal_mask(self_attention)
        output_attentions=False,
    ) -> Tuple[Tensor, Optional[Tensor]]:
        """Input shape: Time(SeqLen) x Batch x Channel"""

        if type(key) == list:
            # 1) Multimodal case
            text_output, text_weights, bsz, tgt_len, embed_dim = self.get_head_output(query, key[0], key_padding_mask[0], layer_state, attn_mask, output_attentions, '_text') # [bsz * self.num_heads, tgt_len, self.head_dim]
            table_output, table_weights, _, _, _ = self.get_head_output(query, key[1], key_padding_mask[1], layer_state, attn_mask, output_attentions, '_table') # [bsz * self.num_heads, tgt_len, self.head_dim]
            img_output, img_weights, _, _, _ = self.get_head_output(query, key[2], key_padding_mask[2], layer_state, attn_mask, output_attentions, '_img') # [bsz * self.num_heads, tgt_len, self.head_dim]

            text_output = self.aggregate_head_output(text_output, bsz, tgt_len, embed_dim) # [tgt_len, bsz, self.embed_dim]
            table_output = self.aggregate_head_output(table_output, bsz, tgt_len, embed_dim) # [tgt_len, bsz, self.embed_dim]
            img_output = self.aggregate_head_output(img_output, bsz, tgt_len, embed_dim) # [tgt_len, bsz, self.embed_dim]

            non_table_batch = key_padding_mask[1].min(dim=2)[0][:, 0] # [bsz]
            non_table_batch = non_table_batch.unsqueeze(0).repeat([table_output.size(0), 1]).unsqueeze(-1) # [tgt_len, bsz, 1]

            non_img_batch = key_padding_mask[2].min(dim=2)[0].min(dim=1)[0] # [bsz]
            non_img_batch = non_img_batch.unsqueeze(0).repeat([img_output.size(0), 1]).unsqueeze(-1) # [tgt_len, bsz, 1]

            alpha = F.relu(torch.tanh(self.alpha_proj(torch.cat([text_output, table_output], dim=-1)))) # [tgt_len, bsz, self.embed_dim]
            beta = F.relu(torch.tanh(self.beta_proj(torch.cat([text_output, img_output], dim=-1)))) # [tgt_len, bsz, self.embed_dim]
            alpha = alpha.masked_fill(non_table_batch, 0.)
            beta = beta.masked_fill(non_img_batch, 0.)

            others_output = alpha * table_output + beta * img_output
            attn_output = text_output + others_output
            attn_weights = [text_weights, table_weights, img_weights]
        else:
            # 2) Unimodal case (original)
            attn_output, attn_weights, bsz, tgt_len, embed_dim = self.get_head_output(query, key, key_padding_mask, layer_state, attn_mask, output_attentions, '')
            attn_output = self.aggregate_head_output(attn_output, bsz, tgt_len, embed_dim)
        return attn_output, attn_weights

    def get_head_output(
        self,
        query,
        key,
        key_padding_mask = None, # decoder_padding_mask(self_attention) / encoder_attn_mask (cross_attention)
        layer_state = None,
        attn_mask = None, # causal_mask(self_attention)
        output_attentions=False,
        cache_key_suffix=''
    ) -> Tuple[Tensor, Optional[Tensor]]:

        static_kv: bool = self.encoder_decoder_attention
        tgt_len, bsz, embed_dim = query.size()
        assert embed_dim == self.embed_dim
        assert list(query.size()) == [tgt_len, bsz, embed_dim]

        if self.encoder_decoder_attention:
            if key.dim() == 4:
                # key : [src_len, num_entities, bsz, embed_dim]
                self.num_entities = key.size(1)

        # get here for encoder decoder cause of static_kv
        if layer_state is not None:  # reuse k,v and encoder_padding_mask
            saved_state = layer_state.get(self.cache_key+cache_key_suffix, {})
            if "prev_key" in saved_state and static_kv:
                # previous time steps are cached - no need to recompute key and value if they are static
                key = None
        else:
            saved_state = None
            layer_state = {}

        q = self.q_proj(query) * self.scaling
        if static_kv:
            if key is None:
                k = v = None
            else:
                k = self.k_proj(key)
                v = self.v_proj(key)
        else:
            k = self.k_proj(query)
            v = self.v_proj(query)

        q = self._shape(q, tgt_len, bsz, 'q')
        
        if k is not None:
            k = self._shape(k, -1, bsz, 'k')
        if v is not None:
            v = self._shape(v, -1, bsz, 'v')

        if saved_state is not None:
            k, v, key_padding_mask = self._use_saved_state(k, v, saved_state, key_padding_mask, static_kv, bsz)
        # Update cache
        if not self.encoder_decoder_attention:
            layer_state[self.cache_key+cache_key_suffix] = {
                "prev_key": k.view(bsz, self.num_heads, -1, self.head_dim),
                "prev_value": v.view(bsz, self.num_heads, -1, self.head_dim),
                "prev_key_padding_mask": key_padding_mask if not static_kv else None,
            }
        else:
            layer_state[self.cache_key+cache_key_suffix] = {
                "prev_key": k.view(self.num_entities, bsz, self.num_heads, -1, self.head_dim),
                "prev_value": v.view(self.num_entities, bsz, self.num_heads, -1, self.head_dim),
                "prev_key_padding_mask": key_padding_mask if not static_kv else None,
            }

        assert k is not None
        src_len = k.size(1)
        attn_weights = torch.bmm(q, k.transpose(1, 2))
        assert attn_weights.size() == (self.num_entities * bsz * self.num_heads, tgt_len, src_len)

        if attn_mask is not None:
            if not self.encoder_decoder_attention:
                attn_weights = attn_weights.view(bsz, self.num_heads, tgt_len, src_len) + attn_mask
            else:
                attn_weights = attn_weights.view(self.num_entities, bsz, self.num_heads, tgt_len, src_len) + attn_mask.transpose(0, 1)
            attn_weights = attn_weights.view(self.num_entities * bsz * self.num_heads, tgt_len, src_len)

        # This is part of a workaround to get around fork/join parallelism not supporting Optional types.
        if key_padding_mask is not None and key_padding_mask.dim() == 0:
            key_padding_mask = None
        if not self.encoder_decoder_attention:
            assert key_padding_mask is None or key_padding_mask.size()[:2] == (bsz, src_len,)
            if key_padding_mask is not None:  # don't attend to padding symbols
                attn_weights = attn_weights.view(bsz, self.num_heads, tgt_len, src_len)
                reshaped = key_padding_mask.unsqueeze(1).unsqueeze(2)
                attn_weights = attn_weights.masked_fill(reshaped, float("-inf"))
                attn_weights = attn_weights.view(bsz * self.num_heads, tgt_len, src_len)
        else:
            assert key_padding_mask is None or key_padding_mask.size()[:3] == (bsz, self.num_entities, src_len,)
            if key_padding_mask is not None:  # don't attend to padding symbols
                attn_weights = attn_weights.view(self.num_entities, bsz, self.num_heads, tgt_len, src_len)
                reshaped = key_padding_mask.transpose(0, 1).unsqueeze(2).unsqueeze(3)
                attn_weights = attn_weights.masked_fill(reshaped, -2.**16)
                attn_weights = attn_weights.view(self.num_entities * bsz * self.num_heads, tgt_len, src_len)

        attn_weights = F.softmax(attn_weights, dim=-1)
        attn_probs = F.dropout(attn_weights, p=self.dropout, training=self.training,)

        assert v is not None
        attn_output = torch.bmm(attn_probs, v)
        assert attn_output.size() == (self.num_entities * bsz * self.num_heads, tgt_len, self.head_dim)

        if self.encoder_decoder_attention:
            r""" parallel mean method """
            attn_output = attn_output.view(self.num_entities, bsz, self.num_heads, tgt_len, self.head_dim)
            if key_padding_mask is not None:
                key_entity_mask = key_padding_mask.min(dim=-1, keepdims=True)[0] # If true : null entity [bsz, num_entities]
                reshaped = key_entity_mask.transpose(0, 1).unsqueeze(2).unsqueeze(3)
                valid_output = attn_output.masked_fill(reshaped, 0.)
                valid_output_sum = valid_output.sum(dim=0)
                valid_output_len = valid_output.masked_fill(~reshaped, 1.).sum(dim=0) # null 0 / not null num_entities [bsz, num_heads, tgt_len, head_dim]

                null_batch = valid_output_len.sum(dim=[1,2,3]) == 0.
                valid_output_len[null_batch] = 1.
                attn_output = valid_output_sum / valid_output_len
            else:
                attn_output = attn_output.mean(dim=0)
            attn_output = attn_output.view(bsz * self.num_heads, tgt_len, self.head_dim)

        if output_attentions:
            attn_weights = attn_weights.view(bsz, self.num_heads, tgt_len, src_len)
        else:
            attn_weights = None
        return attn_output, attn_weights, bsz, tgt_len, embed_dim

    def aggregate_head_output(
        self,
        attn_output,
        bsz,
        tgt_len,
        embed_dim
    ):
        attn_output = attn_output.transpose(0, 1).contiguous().view(tgt_len, bsz, embed_dim)
        attn_output = self.out_proj(attn_output)
        return attn_output


    def _use_saved_state(self, k, v, saved_state, key_padding_mask, static_kv, bsz):
        # saved states are stored with shape (bsz, num_heads, seq_len, head_dim)
        # saved states are stored with shape (num_entities, bsz, num_heads, seq_len, head_dim)

        if "prev_key" in saved_state:
            _prev_key = saved_state["prev_key"]
            assert _prev_key is not None
            prev_key = _prev_key.view(self.num_entities * bsz * self.num_heads, -1, self.head_dim)
            if static_kv:
                k = prev_key
            else:
                assert k is not None
                k = torch.cat([prev_key, k], dim=1)
        if "prev_value" in saved_state:
            _prev_value = saved_state["prev_value"]
            assert _prev_value is not None
            prev_value = _prev_value.view(self.num_entities * bsz * self.num_heads, -1, self.head_dim)
            if static_kv:
                v = prev_value
            else:
                assert v is not None
                v = torch.cat([prev_value, v], dim=1)
        assert k is not None and v is not None
        prev_key_padding_mask: Optional[Tensor] = saved_state.get("prev_key_padding_mask", None)
        if prev_key_padding_mask is not None:
            if static_kv:
                new_key_padding_mask = prev_key_padding_mask
            else:
                new_key_padding_mask = torch.cat([prev_key_padding_mask, key_padding_mask], dim=1)
        else:
            new_key_padding_mask = key_padding_mask
        return k, v, new_key_padding_mask


class BartClassificationHead(nn.Module):
    """Head for sentence-level classification tasks."""

    # This can trivially be shared with RobertaClassificationHead

    def __init__(
        self, input_dim, inner_dim, num_classes, pooler_dropout,
    ):
        super().__init__()
        self.dense = nn.Linear(input_dim, inner_dim)
        self.dropout = nn.Dropout(p=pooler_dropout)
        self.out_proj = nn.Linear(inner_dim, num_classes)

    def forward(self, x):
        x = self.dropout(x)
        x = self.dense(x)
        x = torch.tanh(x)
        x = self.dropout(x)
        x = self.out_proj(x)
        return x


class LearnedPositionalEmbedding(nn.Embedding):
    """
    This module learns positional embeddings up to a fixed maximum size.
    Padding ids are ignored by either offsetting based on padding_idx
    or by setting padding_idx to None and ensuring that the appropriate
    position ids are passed to the forward function.
    """

    def __init__(self, num_embeddings: int, embedding_dim: int, padding_idx: int, offset):
        # Bart is set up so that if padding_idx is specified then offset the embedding ids by 2
        # and adjust num_embeddings appropriately. Other models dont have this hack
        self.offset = offset
        assert padding_idx is not None
        num_embeddings += offset
        super().__init__(num_embeddings, embedding_dim, padding_idx=padding_idx)

    def forward(self, input_ids, use_cache=False):
        """Input is expected to be of size [bsz x seqlen]."""
        bsz, seq_len = input_ids.shape[:2]
        if use_cache:
            positions = input_ids.data.new(1, 1).fill_(seq_len - 1)  # called before slicing
        else:
            # starts at 0, ends at 1-seq_len
            positions = torch.arange(seq_len, dtype=torch.long, device=self.weight.device)
        return super().forward(positions + self.offset)


def LayerNorm(normalized_shape, eps=1e-5, elementwise_affine=True):
    if torch.cuda.is_available():
        try:
            from apex.normalization import FusedLayerNorm

            return FusedLayerNorm(normalized_shape, eps, elementwise_affine)
        except ImportError:
            pass
    return torch.nn.LayerNorm(normalized_shape, eps, elementwise_affine)



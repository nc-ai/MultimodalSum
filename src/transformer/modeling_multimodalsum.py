def shift_tokens_right(input_ids, pad_token_id, bos_token_id, eos_token_id):
    """
        1) a b c d e      -> <s> a b c d
        2) a b c d <\s>   -> <s> a b c d

        3) <s> a b c d    -> <\s> <s> a b c
        4) <s> a b c <\s> -> <\s> <s> a b c
    """
    # """Shift input ids one token to the right, and wrap the last non pad token (usually <eos>)."""
    prev_output_tokens = input_ids.clone()
    index_of_eos = (input_ids.ne(pad_token_id).sum(dim=1) - 1).unsqueeze(-1)
 
    # prev_output_tokens[:, 0] = input_ids.gather(1, index_of_eos).squeeze()
    # prev_output_tokens[:, 1:] = input_ids[:, :-1]

    # Modified to cover all possible cases (1~4)
    if input_ids[0, 0] != bos_token_id:
        prev_output_tokens[:, 0] = torch.full((input_ids.size(0),), bos_token_id, dtype=torch.long)
    else:
        prev_output_tokens[:, 0] = torch.full((input_ids.size(0),), eos_token_id, dtype=torch.long)
    prev_output_tokens[:, 1:] = torch.where(torch.zeros_like(input_ids).scatter_(1, index_of_eos, 1).to(bool), torch.tensor([pad_token_id], device=input_ids.device), input_ids)[:, :-1]
    return prev_output_tokens


def make_padding_mask(input_ids, padding_idx=1):
    """True for pad tokens"""
    padding_mask = input_ids.eq(padding_idx)
    if not padding_mask.any():
        padding_mask = None
    return padding_mask


